#    0123456789012345678901234567890123
a = "Life is too short, You need Python"
print(a[3])
print(a[21]) # 인덱스 21번째 문자
print(a[-3]) # 끝에서 3번째 문자
print(a[0:4]) # 인덱스 0부터 4 바로 앞까지
print(a[5:7]) # 인덱스 5부터 7 바로 앞까지
print(a[:11]) # 인덱스 11바로 앞까지
print(a[12:]) # 인덱스 12부터 끝까지
print(a[:]) # 전체
print(a[10:-2]) # 인덱스 10부터 끝에서 2번째 바로 앞까지